    private void btnPercentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPercentActionPerformed
        double num2 = Double.parseDouble(txtVisor.getText()) / 100;
        double resultado = 0;

        switch (operacao) {
            case "+":
                resultado = num1 + (num1 * num2);
                break;
            case "-":
                resultado = num1 - (num1 * num2);
                break;
            case "X":
                resultado = num1 * (num1 * num2);
                break;
            case "/":
                resultado = num1 / (num1 * num2);
                break;
            default:
                resultado = num1 * num2;
                break;
        }

        operacao = "%";
        resultPerc = resultado;
    }//GEN-LAST:event_btnPercentActionPerformed

