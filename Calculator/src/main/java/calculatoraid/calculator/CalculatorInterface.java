    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        double num2 = Double.parseDouble(txtVisor.getText());
        double resultado = 0;

        switch (operacao) {
            case "+":
                resultado = num1 + num2;
                break;
            case "-":
                resultado = num1 - num2;
                break;
            case "X":
                resultado = num1 * num2;
                break;
            case "/":
                if (num2 != 0) {
                    resultado = num1 / num2;
                } else {
                    txtVisor.setText("Error");
                    return;
                }
                break;

            case "%":
                resultado = resultPerc;
                break;
        }
        txtVisor.setText(String.valueOf(resultado));
        operacaoSelecionada = false;
    }//GEN-LAST:event_btnEqualActionPerformed

